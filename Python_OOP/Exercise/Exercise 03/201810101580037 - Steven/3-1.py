print("Program to show high and low numbers")
num1 = int(input("Enter a number:"))
if num1 > 50:
    print(str(num1) + " is a high number")
elif num1 < 50:
    print(str(num1) + " is a low number")
elif num1 == 50:
    print(str(num1) + " is in the middle")
